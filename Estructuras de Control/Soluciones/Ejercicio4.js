// Declaración de variables
let i = 1;

// Bucle do-while
do {
  // Impresión del número
  console.log(i);

  // Incremento del contador
  i++;
} while (i <= 10);

//En este ejemplo, la condición del bucle es i <= 10. Si esta condición fuera falsa al principio, es decir, 
//si i fuera mayor o igual a 10, el bucle no se ejecutaría ninguna vez. Sin embargo, con un do while, el bucle se ejecutará al menos una vez, ya que la condición se evalúa al final del bucle.
